"""
Temperature conversion
Write a program that can convert a temperature in degrees Celsius to degrees Fahrenheit.
"""
print("\n************* CELSIUS TO FAHRENHEIT CONVERTER *************")
celsius = float(input("\nEnter a temperature in degrees Celsius: "))
fahrenheit = (celsius * 1.8) + 32
print(f"\n {celsius} °C Celsius is {fahrenheit} °F  Fahrenheit")